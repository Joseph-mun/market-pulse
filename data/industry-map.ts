export function getIndustry(id: string): Industry | undefined {
  return INDUSTRIES.find(i => i.id === id);
}

export function getIndustriesForIndicator(indicatorId: string): { industry: Industry; impact: Industry['impacts'][0] }[] {
  const results: { industry: Industry; impact: Industry['impacts'][0] }[] = [];
  for (const industry of INDUSTRIES) {
    const impact = industry.impacts.find(imp => imp.indicatorId === indicatorId);
    if (impact) results.push({ industry, impact });
  }
  return results;
}
